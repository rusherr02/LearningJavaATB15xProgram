package ex_02_Java_Basics;

public class Lab0014_Loops {
    static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.print(i+" "+"\n");
        }
        System.out.println("End");
    }
//    Exit code 0 - Program executed successfully
//    Exit code 130 - stop by user,
}
